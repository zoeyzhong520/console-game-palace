<p><br />冈本吉起是日本资深游戏设计师，1961年生于爱媛县。1981年加入科乐美，1983年转投嘉富康（capcom）公司，2003年自创新公司游戏共和国（Game Republic）任社长。主创过太空战机、1943、大金刚、小精灵、街头霸王zero&1&2、快打旋风、源氏等游戏。

